public class MessagePreviewController extends BaseController
{

  /**
   * Overridden from
   * 
   * @see com.biperf.core.ui.BaseController#onExecute(org.apache.struts.tiles.ComponentContext,
   *      javax.servlet.http.HttpServletRequest, javax.servlet.http.HttpServletResponse,
   *      javax.servlet.ServletContext)
   * @param tileContext
   * @param request
   * @param response
   * @param servletContext
   * @throws Exception
   */
  public void onExecute( ComponentContext tileContext, HttpServletRequest request, HttpServletResponse response, ServletContext servletContext ) throws Exception
  {
    String messageId = request.getParameter( "messageId" );
    Message message = getMessageService().getMessageById( new Long( messageId ) );
    request.setAttribute( "assetCode", message.getCmAssetCode() );
    request.setAttribute( "messageObj", message );
  }

  private MessageService getMessageService() throws Exception
  {
    return (MessageService)getService( MessageService.BEAN_NAME );
  }

}
